key_l = {'q':(0,1),'w':(0,2),'e':(0,3),'r':(0,4),'t':(0,5),'a':(1,1),'s':(1,2),'d':(1,3),'f':(1,4),'g':(1,5),'z':(2,1),'x':(2,2),'c':(2,3),'v':(2,4)}
key_r = {'y':(0,6),'u':(0,7),'i':(0,8),'o':(0,9),'p':(0,10),'h':(1,6),'j':(1,7),'k':(1,8),'l':(1,9),'b':(2,5),'n':(2,6),'m':(2,7)}
l, r = input().split()
l, r = key_l[l], key_r[r]

s = input()
t = 0
for i in s:
    if i in key_l:
        x, y = key_l[i]
        t += abs(l[0] - x) + abs(l[1] - y) + 1
        l = (x, y)
    else:
        x, y = key_r[i]
        t += abs(r[0] - x) + abs(r[1] - y) + 1
        r = (x, y)
print(t)